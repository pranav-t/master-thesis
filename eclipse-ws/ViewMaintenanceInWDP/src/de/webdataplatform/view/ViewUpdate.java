package de.webdataplatform.view;

import de.webdataplatform.system.TableRecord;
import de.webdataplatform.view.operation.ViewOperation;

public class ViewUpdate {


	
	
	
	private ViewTable viewTable;
	
	private ViewOperation viewOperation;
	
	private TableRecord viewRecord;
	
	private boolean trackedTable=false;

	public ViewUpdate(ViewTable viewTable, ViewOperation viewOperation) {
		super();
		this.viewTable = viewTable;
		this.viewOperation = viewOperation;
	}

	
	
	
	
	public ViewTable getViewTable() {
		return viewTable;
	}

	public void setViewTable(ViewTable viewTable) {
		this.viewTable = viewTable;
	}

	public ViewOperation getViewOperation() {
		return viewOperation;
	}

	public void setViewOperation(ViewOperation viewOperation) {
		this.viewOperation = viewOperation;
	}









	@Override
	public String toString() {
		return "ViewUpdate [viewTable=" + viewTable + ", viewOperation="
				+ viewOperation + ", trackedTable=" + trackedTable + "]";
	}





	public TableRecord getViewRecord() {
		return viewRecord;
	}





	public void setViewRecord(TableRecord viewRecord) {
		this.viewRecord = viewRecord;
	}





	public boolean isTrackedTable() {
		return trackedTable;
	}





	public void setTrackedTable(boolean trackedTable) {
		this.trackedTable = trackedTable;
	}







	
	
	
	
	
	

}
