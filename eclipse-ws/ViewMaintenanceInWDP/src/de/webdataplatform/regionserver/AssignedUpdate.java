package de.webdataplatform.regionserver;

import de.webdataplatform.basetable.BaseTableOperation;
import de.webdataplatform.message.SystemID;
import de.webdataplatform.viewmanager.ViewManager;

public class AssignedUpdate {


	
	
	
	private SystemID viewManager;
	
	private BaseTableOperation baseTableOperation;

	
	
	
	
	
	
	public AssignedUpdate(SystemID viewManager, BaseTableOperation baseTableOperation) {
		super();
		this.viewManager = viewManager;
		this.baseTableOperation = baseTableOperation;
	}

	public SystemID getViewManager() {
		return viewManager;
	}

	public void setViewManager(SystemID viewManager) {
		this.viewManager = viewManager;
	}

	public BaseTableOperation getBaseTableOperation() {
		return baseTableOperation;
	}

	public void setBaseTableOperation(BaseTableOperation baseTableOperation) {
		this.baseTableOperation = baseTableOperation;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	

}
